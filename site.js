function openNav() {
   
    document.getElementById("mySidenav").style.marginLeft = "0vw";
    document.getElementById("main").style.marginLeft = "0vw";

  }
  
  function closeNav() {
    document.getElementById("mySidenav").style.marginLeft = "-100vw";
    document.getElementById("main").style.marginLeft= "0vw";
  }